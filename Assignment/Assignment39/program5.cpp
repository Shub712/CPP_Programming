#include<iostream>
using namespace std;

//////////////////////////////////////////////////////
//  Function name : Display
//  Description :   used to display the pattern
//  Input :         Integer
//  Ouput :         Pattern
//  Author :        Shubham Kiran Pawar
//  Date :          08/12/2025
//////////////////////////////////////////////////////

void Display()
{
    static int iCnt = 1;
    static char ch = 'a';
    
    if(iCnt<=6)
    {
        cout<<ch<<"\t";
        iCnt++;
        ch++;
        Display();
    }

    cout<<"\n";
}

//////////////////////////////////////////////////////
//
//  Entry point function of a application
//
//////////////////////////////////////////////////////

int main()
{
    Display();

    return 0 ;
}

