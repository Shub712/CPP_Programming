#include<iostream>
using namespace std;

//////////////////////////////////////////////////////
//  Function name : Display
//  Description :   used to display the pattern
//  Input :         Integer
//  Ouput :         Pattern
//  Author :        Shubham Kiran Pawar
//  Date :          08/12/2025
//////////////////////////////////////////////////////

void Display()
{
    static int iCnt = 5;
    
    if(iCnt>=1)
    {
        cout<<iCnt<<"\t";
        iCnt--;
        Display();
    }

    cout<<"\n";
}

//////////////////////////////////////////////////////
//
//  Entry point function of a application
//
//////////////////////////////////////////////////////

int main()
{
    Display();

    return 0 ;
}

